#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*first;

	if (!lst || !del || !f)
		return (0);
	first = NULL;
	while (lst)
	{
		new = ft_lstnew((*f)(lst->content));
		if (!new)
		{
			while (first)
			{
				new = first->next;
				ft_lstdelone(first, del);
				first = new;
			}
		}
		ft_lstadd_back(&first, new);
		lst = lst->next;
	}
	return (first);
}
